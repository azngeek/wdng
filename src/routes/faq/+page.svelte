	<h2>Frequently asked questions</h2>

	<h3>1) Est-ce que le mariage aura lieu en cas de mauvais temps?</h3>
    <p>Oui, il aura lieu par tous les temps. Le Markus Wasmeier Freilichtmuseum offre un lieu de cérémonie couvert en cas de pluie. Par beau temps, la cérémonie se déroulera en plein air. Le repas du soir et la piste de danse se trouveront, quant à eux, dans la salle principale quelque soit le temps.</p>

    <h3>2) Où puis-je garer ma voiture ?</h3>
    <p>Le musée a son propre parking dont les invités pourront disposer. 
        Une fois garé, il vous faudra parcourir environ 500 mètres à pied  (voir photo ci-dessous) pour rejoindre le hameau bavarois où se dérouleront les festivités.
        Conseil pour les filles en talons hauts: prenez une deuxième paire de chaussures plates. Il y aura un vestiaire sur place où déposer vos affaires.
        </p>
    
    <h3>3) Est-il possible de venir au mariage en transport public? </h3>
    <p>Oui, il y a un train régional  direct partant de la gare centrale de Munich. Il s’arrête à 200 m du Markus Wasmeier Freilichtmuseum.
        Train:  Bayerische Regiobahn
        Arrêt: Fischhausen-Neuhaus (un arrêt après Schliersee)
        Durée de trajet: 1h 
        </p>

    <h3>4) Je souhaite rentrer à Munich après la fête: y a-t-il une navette de nuit pour Munich? </h3>
    <p>
        Un bus sera organisé en fin de soirée pour conduire les invités qui le souhaitent au centre de Munich. Merci de nous indiquer dans le formulaire si vous souhaitez utiliser cette navette.
    </p>

    <h3>5) Je passe la nuit dans un hôtel environnant: y a-t-il une navette pour retourner à l'hôtel pendant la nuit ? </h3>
    <p>
        Oui une navette passera par les hôtels de Schliersee et environs pour conduire les invités en fin de soirée.
    </p>

    <h3>6) Quel est le dress code?</h3>
    <p>
        Il n’y a pas de dress code. C'est tout simplement l’occasion pour vous de sortir vos belles tenues. Portez ce qui vous fait plaisir et choisissez la tenue dans laquelle vous vous sentez le plus beau.
Note annexe: les mariés ne seront pas en habits traditionnels bavarois. 
    </p>

    <h3>7) Puis-je venir avec mes enfants ?</h3>
    <p>
        Pour des raisons de place, la fête sera “sans enfants” à l’exception de la famille de premier degré des mariés. Toutefois, nous comprenons que certains d’entre-vous viennent de loin et souhaitent combiner le mariage avec des vacances en famille. Si c'est le cas, veuillez nous en informer à l'avance via le formulaire (RSVP) afin que nous puissions organiser un système de garde.
    </p>

    <h3>8) Je souhaite faire un discours ou une surprise aux mariés, à qui dois-je m’adresser?</h3>
    <p>
        Super! Adressez-vous directement aux témoins (link to page) 
    </p>

    <h3>9) Que souhaitent les mariés comme cadeau de mariage? </h3>
    <p>
        Le plus beau cadeau sera votre présence. D’autant plus que vous venez tous de loin! Si vous tenez tout de même à offrir quelque chose aux mariés, une cagnotte sera mise à la disposition des invités pour ceux qui souhaitent contribuer à leur voyage de noce.
    </p>


